<div>
    The name’s Mattias Evangelista. I’m 18 years old and was born and raised in the North Cascades, specifically Mt. Baker. I live year round in a very small
    town called Glacier, which lies right at the base of Mt. Baker and honestly there’s nowhere else in the world I would rather live. During the winters I try
    to go to school and work as little as possible and ski as much as possible. For me skiing is something that defines me as a human being and is one of
    biggest influences in my life. There’s no where else in the world where I feel more at home and alive than in the mountains and no other sport can provide
    me with the same satisfaction and exhilaration that skiing can. From hitting jumps, to exploring a new line in the backcountry, to shredding pow covered
    pillows, to filming and editing I get enjoyment from every aspect of skiing, and I work very hard to the be best skier I can be in all those aspects.
    During the summer I try to go to school a little more and work a little more…but not too much! When I’m not working or going to school I’m usually riding
    my mountain bike or building trails to ride my bike on in the hills behind my house. I would love to give a huge shout out to all my sponsors; Surface
    Skis, Joystick Poles, Backcountry Access, Rime Knits, Pow Gloves, INNATE, Scotts Ski Service and of course TREW Gear! But, more importantly I would like to
    give a huge thanks to my parents, my girlfriend, and of course my brother Micah. Thanks so much for supporting me on this crazy adventure, and helping me
    accomplish my dream of never having to work a “real job!”
</div>
