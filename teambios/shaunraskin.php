<div>
    Shaunie shreds big mountain on her teles harder and faster than your can strum the fake strings on your xbox guitar. She skis with a grace and power that
    only a lady can bring to the mountains. Growing up skiing in NY, at the young age at 2 she had snow under her skis; and decades later she has realized her
    dream of being a professional skier. She currently resides in Utah. When she isn’t skiing, she rips on her mountain bike, plays guitar, makes guys really
    jealous of her athleticism, and eats tofu.
