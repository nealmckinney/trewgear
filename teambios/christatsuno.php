    All have been ways to describe my passion for skiing and who I am as a result. But my story is far from being complete. After living many seasons with no
    concrete direction save for the premise of chasing powder, I have have taken on a new role in life and in my skiing. Skiing is a lifestyle (don't let them
    convince you otherwise!), and it is my chance to pass on this accumulation of road-worthy knowledge to a new generation. This season I have been asked to
    help coach the AVSC Adaptive program in Aspen, CO and it is through this position I can influence these Paralympic hopefuls to look beyond the racing world
