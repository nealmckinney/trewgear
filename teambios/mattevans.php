<div>
    My name is Matt. I was born and raised in Crested Butte, so naturally, I live for situations where safety is, well, questionable. I came from a ski family,
    and for as long as I can remember I have been looking for adrenaline. Having two very influential older brothers, I was frequently in situations that were
    out of my comfort zone. I started out skiing in ski school with the rest of the kids my age, but it didn't take long to realize I wanted more. When the
    public school finally got a ski team, I started competing, and traveling the country. Though flipping through the air at park comps. is fun, I found my
    true calling in extreme skiing. These competitions not only scare me more than park skiing, they allow me to enjoy the mountains in ways I never thought
    possible. My love for gnar, and the extreme scenarios I face with my close friends is what I love. The high fives and the stoke at the bottom is what I
    live for.
</div>
