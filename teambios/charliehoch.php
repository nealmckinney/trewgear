<div>
    Winters in Silverton, CO may not be for everyone, but for Charlie Hoch, there couldn’t be a better home base when the snow starts flying. Having grown up
    nearby in Pagosa Springs after his family moved from sunny Florida at a young age, Charlie began to polish his skills at Wolf Creek. He has worked
    tirelessly to get to some of the biggest terrain in the U.S. and most recently South America this past summer, exploring Chile and Argentina. After
    sustaining a brutal season-ending knee injury in early 2011, Charlie came back with a vengeance in 2012 and showed the world just what kind of
    determination he had to go along with his talent. After taking home a first-place finish at Kirkwood in the North Face Masters last winter, Charlie is well
    on his way to establishing himself as an elite big mountain rider. His biggest reward however may have been his trip last spring to Alaska, where he made a
    dream come true, camping on glaciers and seeking out new terrain he never thought he would see the likes of up close. Now that Charlie has had a taste of
    something that special, it only fuels his desire to show the world he deserves to be in the conversation even more.
</div>
